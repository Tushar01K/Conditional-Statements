public class Percentage{

    public static void main(String[] args){
        int a=78;
        int b=78;
        int c=32;
        int d=98;
        int e=23;
        int sum =a+b+c+d+e;
        System.out.println(sum);
        float percentage=(sum)/5*100;
        System.out.println(percentage);
    }
}